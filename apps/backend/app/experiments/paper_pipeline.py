"""Paper-ready experiment pipeline for reproducible multi-seed result bundles."""
from __future__ import annotations

import csv
import json
import shutil
import sys
import hashlib
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Tuple

from . import ROOT_DIR, OUTPUT_DIR
from .config import ExperimentConfig
from .contracts import PublicationBundleManifest
from .runner import run_experiment_sync
from .stats_engine import ExperimentStatsEngine
from app.validation.runner import run_validation


PUBLICATION_LAYOUT_VERSION = "1.0"


@dataclass
class VariantResult:
    variant_id: str
    seed: int
    output_name: str
    config_hash: str
    metrics: Dict[str, Any]
    provenance: Dict[str, Any]
    validation: Dict[str, Any] | None


def _resolve_path(path_value: str) -> Path:
    raw = Path(path_value)
    if raw.is_absolute():
        return raw
    return ROOT_DIR / raw


def _deep_merge(base: Dict[str, Any], override: Dict[str, Any]) -> Dict[str, Any]:
    merged = dict(base)
    for key, value in override.items():
        if isinstance(value, dict) and isinstance(merged.get(key), dict):
            merged[key] = _deep_merge(merged[key], value)
        else:
            merged[key] = value
    return merged


def _coerce_config_hash(result: Dict[str, Any]) -> str:
    existing = result.get("config_hash")
    if isinstance(existing, str) and existing.strip():
        return existing.strip()
    payload = result.get("config", {})
    return hashlib.sha256(json.dumps(payload, sort_keys=True).encode("utf-8")).hexdigest()


def _extract_provenance(result: Dict[str, Any]) -> Dict[str, Any]:
    provenance = result.get("provenance")
    if isinstance(provenance, dict):
        return dict(provenance)
    metadata = result.get("metadata")
    if not isinstance(metadata, dict):
        metadata = {}
    config = result.get("config")
    if not isinstance(config, dict):
        config = {}
    return {
        "generated_at": metadata.get("generated_at"),
        "engine": metadata.get("engine"),
        "engine_version": metadata.get("engine_version"),
        "seed": metadata.get("seed", config.get("seed")),
        "floor_plan_id": metadata.get("floor_plan_id", config.get("floor_plan_id")),
        "floor_plan_revision": metadata.get("floor_plan_revision"),
    }


def _persist_validation(output_name: str, validations: Dict[str, Any]) -> None:
    output_path = OUTPUT_DIR / f"{output_name}.json"
    if not output_path.exists():
        return
    data = json.loads(output_path.read_text(encoding="utf-8-sig"))
    data["validation"] = validations
    output_path.write_text(json.dumps(data, indent=2), encoding="utf-8")


def _prepare_publication_layout(artifact_dir: Path) -> Dict[str, Path]:
    layout = {
        "root": artifact_dir,
        "manifests": artifact_dir / "manifests",
        "summaries": artifact_dir / "summaries",
        "tables": artifact_dir / "tables",
        "figures": artifact_dir / "figures",
        "metadata": artifact_dir / "metadata",
        "raw_runs": artifact_dir / "raw_runs",
    }
    for folder in layout.values():
        folder.mkdir(parents=True, exist_ok=True)
    return layout


def _write_publication_readme(artifact_dir: Path, suite_name: str, run_count: int, validated: bool) -> Path:
    readme = artifact_dir / "README.md"
    validated_text = "enabled" if validated else "disabled"
    readme.write_text(
        "\n".join(
            [
                f"# Paper Results Bundle: {suite_name}",
                "",
                "This folder is generated by the paper-results pipeline and is publication-ready.",
                "",
                "## Structure",
                "- `manifests/`: run-level manifests (`runs_manifest.csv/json`)",
                "- `summaries/`: statistical summaries and comparisons",
                "- `tables/`: publication-ready tabular extracts",
                "- `figures/`: placeholder location for rendered charts/plots",
                "- `metadata/`: reproducibility metadata and config snapshots",
                "- `raw_runs/`: copied per-run raw JSON outputs",
                "",
                "## Build Metadata",
                f"- Runs: {run_count}",
                f"- Validation: {validated_text}",
                f"- Layout version: {PUBLICATION_LAYOUT_VERSION}",
                "",
            ]
        ),
        encoding="utf-8",
    )
    return readme


def _copy_raw_runs(rows: List[Dict[str, Any]], raw_runs_dir: Path) -> Tuple[int, List[str]]:
    copied = 0
    missing: List[str] = []
    for row in rows:
        output_name = str(row.get("output_name", "")).strip()
        if not output_name:
            continue
        source = OUTPUT_DIR / f"{output_name}.json"
        destination = raw_runs_dir / f"{output_name}.json"
        if source.exists():
            shutil.copy2(source, destination)
            copied += 1
        else:
            missing.append(output_name)
    return copied, missing


def _write_metrics_table(summary: Dict[str, Any], tables_dir: Path) -> Path:
    table_path = tables_dir / "variant_metrics_summary.csv"
    with table_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(
            handle,
            fieldnames=["variant_id", "metric", "n", "mean", "std", "ci95_low", "ci95_high"],
        )
        writer.writeheader()
        for variant_id, metric_map in summary.get("metrics", {}).items():
            for metric_name, metric_payload in metric_map.items():
                writer.writerow(
                    {
                        "variant_id": variant_id,
                        "metric": metric_name,
                        "n": metric_payload.get("n"),
                        "mean": metric_payload.get("mean"),
                        "std": metric_payload.get("std"),
                        "ci95_low": metric_payload.get("ci95", [None, None])[0],
                        "ci95_high": metric_payload.get("ci95", [None, None])[1],
                    }
                )
    return table_path


def _write_reproducibility_metadata(
    metadata_dir: Path,
    batch_path: Path,
    base_config_path: Path,
    payload: Dict[str, Any],
    seeds: List[int],
    validate: bool,
) -> Dict[str, Path]:
    generated_at = datetime.now(timezone.utc).isoformat()
    batch_snapshot = metadata_dir / "batch_config_snapshot.json"
    batch_snapshot.write_text(json.dumps(payload, indent=2), encoding="utf-8")

    base_snapshot = metadata_dir / "base_config_snapshot.json"
    base_snapshot.write_text(base_config_path.read_text(encoding="utf-8-sig"), encoding="utf-8")

    repro = {
        "layout_version": PUBLICATION_LAYOUT_VERSION,
        "generated_at": generated_at,
        "batch_config_path": str(batch_path),
        "base_config_path": str(base_config_path),
        "python_version": sys.version,
        "seed_count": len(seeds),
        "seeds": seeds,
        "validation_enabled": validate,
    }
    repro_path = metadata_dir / "reproducibility.json"
    repro_path.write_text(json.dumps(repro, indent=2), encoding="utf-8")
    return {
        "batch_snapshot": batch_snapshot,
        "base_snapshot": base_snapshot,
        "reproducibility": repro_path,
    }


def run_paper_pipeline(
    batch_config_path: str,
    validate: bool = True,
    validate_eth: bool = False,
    eth_dataset_root: str | None = None,
    eth_download_if_missing: bool = False,
    eth_dataset_url: str | None = None,
    artifacts_root: str | None = None,
    copy_run_outputs: bool = True,
) -> Dict[str, Any]:
    batch_path = _resolve_path(batch_config_path)
    if not batch_path.exists():
        raise FileNotFoundError(f"Paper batch config not found: {batch_path}")
    payload = json.loads(batch_path.read_text(encoding="utf-8-sig"))

    suite_name = str(payload.get("name", "paper_suite"))
    base_config_path = _resolve_path(str(payload["base_config"]))
    if not base_config_path.exists():
        raise FileNotFoundError(f"Base experiment config not found: {base_config_path}")
    base_config = json.loads(base_config_path.read_text(encoding="utf-8-sig"))

    raw_seeds = payload.get("seeds", [42])
    if not isinstance(raw_seeds, list) or not raw_seeds:
        raise ValueError("Paper pipeline requires a non-empty 'seeds' list")
    seeds = sorted({int(v) for v in raw_seeds})

    variants = payload.get("variants", [{"id": "baseline", "overrides": {}}])
    if not isinstance(variants, list) or not variants:
        raise ValueError("Paper pipeline requires a non-empty 'variants' list")

    all_results: List[VariantResult] = []
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    for variant in variants:
        variant_id = str(variant.get("id", "variant"))
        overrides = variant.get("overrides", {}) or {}
        for seed in seeds:
            run_payload = _deep_merge(base_config, overrides)
            run_payload["seed"] = seed
            run_payload["name"] = f"{suite_name}__{variant_id}__seed_{seed}"
            run_cfg = ExperimentConfig(**run_payload)
            result = run_experiment_sync(run_cfg)

            validations = None
            if validate:
                validations = run_validation(
                    str(OUTPUT_DIR / f"{result['config']['name']}.json"),
                    include_eth=validate_eth,
                    eth_dataset_root=eth_dataset_root,
                    eth_download_if_missing=eth_download_if_missing,
                    eth_dataset_url=eth_dataset_url,
                )
                _persist_validation(result["config"]["name"], validations)

            all_results.append(
                VariantResult(
                    variant_id=variant_id,
                    seed=seed,
                    output_name=result["config"]["name"],
                    config_hash=_coerce_config_hash(result),
                    metrics=result.get("metrics", {}),
                    provenance=_extract_provenance(result),
                    validation=validations,
                )
            )

    timestamp = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    artifact_root = Path(artifacts_root) if artifacts_root else (ROOT_DIR / "artifacts" / "paper_results")
    artifact_dir = artifact_root / f"{suite_name}_{timestamp}"
    layout = _prepare_publication_layout(artifact_dir)

    rows: List[Dict[str, Any]] = []
    grouped: Dict[str, Dict[str, List[float]]] = {}

    for item in all_results:
        total_time = float(item.metrics.get("total_evacuation_time", 0.0) or 0.0)
        safety_score = float(item.metrics.get("safety_score", 0.0) or 0.0)
        survival_probability = float(item.metrics.get("survival_probability", 0.0) or 0.0)
        validation_overall = None
        validation_status = None
        if item.validation:
            validation_overall = item.validation.get("summary", {}).get("overall_score", item.validation.get("overall_score"))
            validation_status = item.validation.get("summary", {}).get("status")

        rows.append(
            {
                "variant_id": item.variant_id,
                "seed": item.seed,
                "output_name": item.output_name,
                "config_hash": item.config_hash,
                "engine_version": item.provenance.get("engine_version"),
                "floor_plan_id": item.provenance.get("floor_plan_id"),
                "floor_plan_revision": item.provenance.get("floor_plan_revision"),
                "total_evacuation_time": total_time,
                "safety_score": safety_score,
                "survival_probability": survival_probability,
                "validation_overall": validation_overall,
                "validation_status": validation_status,
            }
        )

        grouped.setdefault(item.variant_id, {}).setdefault("total_evacuation_time", []).append(total_time)
        grouped.setdefault(item.variant_id, {}).setdefault("safety_score", []).append(safety_score)
        grouped.setdefault(item.variant_id, {}).setdefault("survival_probability", []).append(survival_probability)

    runs_csv_path = layout["manifests"] / "runs_manifest.csv"
    runs_json_path = layout["manifests"] / "runs_manifest.json"
    with runs_csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()) if rows else ["variant_id", "seed", "output_name"])
        writer.writeheader()
        writer.writerows(rows)
    runs_json_path.write_text(json.dumps(rows, indent=2), encoding="utf-8")

    summary: Dict[str, Any] = {
        "suite_name": suite_name,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "seed_count": len(seeds),
        "variant_count": len(variants),
        "metrics": {},
        "comparisons": {},
    }

    for variant_id, metrics in grouped.items():
        summary["metrics"][variant_id] = {}
        for metric_name, values in metrics.items():
            summary["metrics"][variant_id][metric_name] = ExperimentStatsEngine.compute_confidence_intervals(values)
            summary["metrics"][variant_id][metric_name]["n"] = len(values)

    reference_variant = "full_model" if "full_model" in grouped else (list(grouped.keys())[0] if grouped else None)
    if reference_variant:
        for variant_id, metrics in grouped.items():
            if variant_id == reference_variant:
                continue
            summary["comparisons"][f"{variant_id}_vs_{reference_variant}"] = {}
            for metric_name in ("total_evacuation_time", "safety_score", "survival_probability"):
                lhs = metrics.get(metric_name, [])
                rhs = grouped[reference_variant].get(metric_name, [])
                summary["comparisons"][f"{variant_id}_vs_{reference_variant}"][metric_name] = (
                    ExperimentStatsEngine.compute_statistical_significance(lhs, rhs)
                )

    summary_path = layout["summaries"] / "stats_summary.json"
    summary_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")

    table_path = _write_metrics_table(summary, layout["tables"])
    copied_run_count = 0
    missing_run_outputs: List[str] = []
    if copy_run_outputs:
        copied_run_count, missing_run_outputs = _copy_raw_runs(rows, layout["raw_runs"])

    metadata_paths = _write_reproducibility_metadata(
        layout["metadata"],
        batch_path,
        base_config_path,
        payload,
        seeds,
        validate,
    )
    readme_path = _write_publication_readme(artifact_dir, suite_name, len(rows), validate)
    figures_readme = layout["figures"] / "README.md"
    figures_readme.write_text(
        "Place publication figures here (e.g., evacuation_time_boxplot.png, survival_curve.svg).\n",
        encoding="utf-8",
    )

    publication_manifest = PublicationBundleManifest(
        layout_version=PUBLICATION_LAYOUT_VERSION,
        suite_name=suite_name,
        generated_at=datetime.now(timezone.utc).isoformat(),
        run_count=len(rows),
        validation_enabled=validate,
        copy_run_outputs=copy_run_outputs,
        copied_run_count=copied_run_count,
        missing_run_outputs=missing_run_outputs,
        seeds=seeds,
        variants=[str(variant.get("id", "variant")) for variant in variants],
        paths={
            "manifests_dir": str(layout["manifests"]),
            "summaries_dir": str(layout["summaries"]),
            "tables_dir": str(layout["tables"]),
            "figures_dir": str(layout["figures"]),
            "metadata_dir": str(layout["metadata"]),
            "raw_runs_dir": str(layout["raw_runs"]),
            "runs_csv": str(runs_csv_path),
            "runs_json": str(runs_json_path),
            "stats_summary": str(summary_path),
            "metrics_table": str(table_path),
            "reproducibility": str(metadata_paths["reproducibility"]),
            "readme": str(readme_path),
        },
        runs=[
            {
                "variant_id": item.variant_id,
                "seed": item.seed,
                "output_name": item.output_name,
                "config_hash": item.config_hash,
                "engine_version": item.provenance.get("engine_version"),
                "floor_plan_id": item.provenance.get("floor_plan_id"),
                "floor_plan_revision": item.provenance.get("floor_plan_revision"),
                "validation_status": (
                    item.validation.get("summary", {}).get("status")
                    if isinstance(item.validation, dict)
                    else None
                ),
            }
            for item in all_results
        ],
    ).to_dict()
    publication_manifest_path = layout["metadata"] / "publication_manifest.json"
    publication_manifest_path.write_text(json.dumps(publication_manifest, indent=2), encoding="utf-8")

    return {
        "suite_name": suite_name,
        "artifact_dir": str(artifact_dir),
        "layout_version": PUBLICATION_LAYOUT_VERSION,
        "publication_manifest": str(publication_manifest_path),
        "runs_csv": str(runs_csv_path),
        "runs_json": str(runs_json_path),
        "stats_summary": str(summary_path),
        "metrics_table": str(table_path),
        "copied_run_count": copied_run_count,
        "missing_run_outputs": missing_run_outputs,
        "run_count": len(rows),
    }
